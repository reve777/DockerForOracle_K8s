-- 1. 建立商品表 (Product)
CREATE TABLE PRODUCT (
    ID NUMBER(19,0) GENERATED BY DEFAULT ON NULL AS IDENTITY,
    NAME VARCHAR2(255 CHAR),
    PRICE BINARY_DOUBLE,
    PRIMARY KEY (ID)
);

-- 2. 建立訂單紀錄表 (OrderRecord)
-- 注意：Oracle 中 "ORDER" 是保留字，建議表名使用 ORDER_RECORD
CREATE TABLE ORDER_RECORD (
    ID NUMBER(19,0) GENERATED BY DEFAULT ON NULL AS IDENTITY,
    ORDER_ID VARCHAR2(255 CHAR),
    PRODUCT_ID NUMBER(19,0),
    AMOUNT BINARY_DOUBLE,
    STATUS VARCHAR2(50 CHAR),
    KAFKA_KEY VARCHAR2(255 CHAR),
    PRIMARY KEY (ID)
);

