CREATE TABLE ivan_txn_log (
    txn_id          VARCHAR(64) PRIMARY KEY, -- 交易唯一編號 (UUID 或 Snowflake ID)
    from_account    VARCHAR(20) NOT NULL,    -- 轉出帳號
    to_account      VARCHAR(20) NOT NULL,    -- 轉入帳號
    amount          DECIMAL(18, 2) NOT NULL, -- 交易金額
    currency        VARCHAR(3) DEFAULT 'TWD',-- 幣別
    status          VARCHAR(10),             -- 狀態: SUCCESS, FAIL, PROCESSING
    created_at      TIMESTAMP DEFAULT CURRENT_TIMESTAMP, -- 交易時間
    remark          VARCHAR(255)             -- 備註 (例如: "Redis-Lua Simulation")
);

-- 建立索引以利後續查詢
CREATE INDEX idx_txn_from ON ivan_txn_log(from_account);
CREATE INDEX idx_txn_to ON ivan_txn_log(to_account);
CREATE INDEX idx_txn_created ON ivan_txn_log(created_at);